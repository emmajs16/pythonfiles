# Constants
LBS_TO_KG = 0.454

# get weight in pounds from user
weight_lbs = int((input("Please enter weight in pounds:")))

# convert weight to kg
weight_kg = weight_lbs * LBS_TO_KG

# display results
print(weight_lbs, "pounds is equal to", weight_kg, "kilograms.")