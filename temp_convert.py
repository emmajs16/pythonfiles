# Problem: change a temperture from celsius to fahrenheit

# Get the temperture in celsius
temp_c = int(input("Please enter a temperture in degrees celsius:"))

# Convert temperture to fahrenheit
temp_f = (9/5) * temp_c + 32

# Print results

print(temp_c,"degrees Celsius is equal to", temp_f, "degrees Fahrenheit.")